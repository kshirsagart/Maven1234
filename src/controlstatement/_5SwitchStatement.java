package controlstatement;

public class _5SwitchStatement {

	public static void main(String[] args) {
		int age=18;
		
		switch(age) {
		case 1:System.out.println("hello");
		break;
		case 2:System.out.println("java");
		break;
		case 18:System.out.println("Valid");
		break;
		default:System.out.println("Not match");
		
		}

	}

}
